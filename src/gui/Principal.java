    /**
     * recarga la lista de empresas 
     * con respecto al archivo
     */
    public void loadEmpresas(){
        Object[] obs = new Object[Archivos.empresas.size()];
        int i = 0;
        for(Empresa e : Archivos.empresas){
             obs[i++] = e.getNombre();
        }
        empresas_list.setListData(obs);
    }
