    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        TreeModel treeModel = genealogyTree.getModel();
        TipoActivo ta = (TipoActivo)genealogyTree.getLastSelectedPathComponent();
        nuevoActivo.addTipo(ta);
        loadListaTipos();
    }//GEN-LAST:event_jButton2MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        ListModel listmodel = listaTiposJL.getModel();
        int index = listaTiposJL.getSelectedIndex();
        if(index == -1) return;
        String tipoSelected = (String) listmodel.getElementAt(index);
        
        for (Iterator<TipoActivo> it = nuevoActivo.getTipos().iterator(); it.hasNext(); ){
            TipoActivo ta = it.next();
            if( ta.toString().equals(tipoSelected) ){
                it.remove();
            }
        }
        loadListaTipos();
    }//GEN-LAST:event_jButton3MouseClicked

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        boolean correct = true;
        if(nombreActivoJTF.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Por favor ingrese el nombre del activo", "Error", JOptionPane.ERROR_MESSAGE);
            correct = false;
        }                JOptionPane.showMessageDialog(this, "El nombre del activo ya existe", "Error", JOptionPane.ERROR_MESSAGE);

        for(Activo a : empresa.getActivos()){
            if(a.getNombre().equals(nombreActivoJTF.getText())){
                correct = false;
                break;
            }
        }
        if(correct){
            nuevoActivo.setNombre(nombreActivoJTF.getText());
            empresa.addActivo(nuevoActivo);
            ((ActivosEmpresa)parent).loadActivos();
            Archivos.guardarArchivo();
            this.dispose();
        }
    }//GEN-LAST:event_jButton1MouseClicked

