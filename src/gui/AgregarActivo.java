    /**
     * recarga la lista de tipos que se seleccionan
     * para el nuevo activo
     */
    private void loadListaTipos(){
        Object[] obs = new Object[nuevoActivo.getTipos().size()];
        int i = 0;
        for(TipoActivo ta : nuevoActivo.getTipos()){
             obs[i++] = ta.toString();
        }
        listaTiposJL.setListData(obs);
    }
